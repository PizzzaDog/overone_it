package n_bogdan.lesson5;

public class PassContainer {
    public final String pass;
    public final String pass2;

    public PassContainer(String pass, String pass2) {
        this.pass = pass;
        this.pass2 = pass2;
    }

    public String getPass() {
        return pass;
    }

    public String getPass2() {
        return pass2;
    }
}

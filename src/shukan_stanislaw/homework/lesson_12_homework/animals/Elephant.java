package shukan_stanislaw.homework.lesson_12_homework.animals;

public class Elephant {
    String name;
    int age;

    public Elephant(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
